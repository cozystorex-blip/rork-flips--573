export function getProductImageUrl(title: string, category: string): string {
  const lower = title.toLowerCase().trim();

  for (const [keyword, url] of PRODUCT_IMAGES) {
    if (lower === keyword) return url;
  }

  for (const [keyword, url] of PRODUCT_IMAGES) {
    if (lower.includes(keyword)) {
      return url;
    }
  }

  for (const [keyword, url] of PRODUCT_IMAGES) {
    if (keyword.includes(lower)) {
      return url;
    }
  }

  const words = lower.split(/[\s,()]+/).filter(w => w.length >= 4);
  for (const word of words) {
    for (const [keyword, url] of PRODUCT_IMAGES) {
      if (keyword === word) {
        return url;
      }
    }
  }

  return CATEGORY_FALLBACK_IMAGES[category] ?? CATEGORY_FALLBACK_IMAGES.other;
}
