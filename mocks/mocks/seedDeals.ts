export const DEAL_POSTER_MAP: Record<string, { name: string; avatar: string }> = {};

function assignPoster(dealId: string, profileIndex: number): string | null {
  const profile = mockProfiles[profileIndex % mockProfiles.length];
  if (profile) {
    DEAL_POSTER_MAP[dealId] = { name: profile.name, avatar: profile.avatar };
    return profile.id;
  }
  return null;
}

const now = Date.now();
function hoursAgo(h: number): string {
  return new Date(now - h * 3600000).toISOString();
}
function daysAgo(d: number, extraHours = 0): string {
  return new Date(now - (d * 24 + extraHours) * 3600000).toISOString();
}

